#  Memory Position
# To describe how variety take up memory
name = 'Jack'  # define a variable and assign it.
print(id(name))  # use a function id() to show which memory address was take up to save the variety assignment
name1 = name  # create a variety and assign it by other variety
print(id(name1), id(name))
# different variety all point to the same MEM_address. In other words, it means all variety both  point to the value

